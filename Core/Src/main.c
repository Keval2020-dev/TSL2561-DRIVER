	  //Poll the sensor for Reading for both spectrum bands
	  TSL2561_getLuminosity(&Broadband, &Ir);
	  Total = TSL2561_calculateLux(Broadband, Ir);

	  //Print out Lux value and interrupt counter
	  printf("Reading In Lux : %d,Counter Value: %d\n\r",Total,Int_Counter);


	  //According to this example threshold occurs above 2000 lux with LED light , if candle light used threshold occurs at 100 lux

