/**
  * @brief I2c master write function
  * @param i2c_addr slave address
  * @param reg_addr slave register address which needs to be write
  * @param *reg_data data buffer which contains data to be written
  * @param length   No. of bytes needs to be written
  * @retval 0 success , -1 error
  */
int8_t i2c_reg_write(uint8_t i2c_addr, uint8_t reg_addr, uint8_t *reg_data, uint16_t length)
{
  int8_t *buf;
  buf = malloc(length +1);
  buf[0] = reg_addr;
  memcpy(buf +1, reg_data, length);

  if(HAL_I2C_Master_Transmit(&hi2c1, (i2c_addr << 1), (uint8_t*)buf, length + 1, HAL_MAX_DELAY) != HAL_OK)
	  {
	      printf("I2C Write Error\n");
	  	  return -1;
	  }

  free(buf);
  return 0;
}



/**
  * @brief I2c master read function
  * @param i2c_addr slave address
  * @param reg_addr slave register address which needs to be read
  * @param *reg_data read data will be stored in this variable
  * @param length   No. of bytes needs to be read
  * @retval 0 success , -1 error
  */
int8_t i2c_reg_read(uint8_t i2c_addr, uint8_t reg_addr, uint8_t *reg_data, uint16_t length)
{

if(HAL_I2C_Master_Transmit(&hi2c1, (i2c_addr << 1), &reg_addr, 1, 10) != HAL_OK) {
    printf("I2C Read Error\n");
	  return -1;
}

  if(HAL_I2C_Master_Receive(&hi2c1, (i2c_addr << 1) | 0x01, reg_data, length, 10) != HAL_OK){
      printf("I2C Read Error\n");
  	  return -1;
  }


  return 0;
}

